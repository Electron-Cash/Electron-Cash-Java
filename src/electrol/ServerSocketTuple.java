package electrol;

public class ServerSocketTuple {
	private String server;
	private Socket socket;
	public String getServer() {
		return server;
	}
	public void setServer(String server) {
		this.server = server;
	}
	public Socket getSocket() {
		return socket;
	}
	public void setSocket(Socket socket) {
		this.socket = socket;
	}
}
